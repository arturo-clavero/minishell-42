#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s || fd < 0 || fd > 1023)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}
